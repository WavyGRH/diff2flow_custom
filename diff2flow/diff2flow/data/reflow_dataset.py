"""
Reflow Dataset for Trajectory Straightening.

Loads pre-computed noise-image pairs generated from a pre-trained model.
These pairs are used for Reflow training, which straightens ODE trajectories
for faster few-step inference.

Expected directory structure:
    root_dir/
    ├── noise/
    │   ├── 000001.pt
    │   └── ...
    └── images/
        ├── 000001.pt
        └── ...
"""

from __future__ import annotations

from pathlib import Path

import torch

from .base_dataset import BaseDataset


class ReflowDataset(BaseDataset):
    """Dataset for pre-computed noise-image pairs for Reflow training.

    Each sample is a paired (noise, image) generated by running inference
    on a pre-trained model. The noise is the initial Gaussian seed, and
    the image is the generated output.

    Args:
        root_dir: Directory containing noise/ and images/ subdirectories.
        resolution: Not used (latents are pre-computed).
    """

    def __init__(self, root_dir: str, **kwargs):
        super().__init__(root_dir, resolution=(512, 512))

    def _load_samples(self) -> list:
        """Find paired noise-image files."""
        noise_dir = self.root_dir / "noise"
        image_dir = self.root_dir / "images"

        if not noise_dir.exists() or not image_dir.exists():
            return []

        samples = []
        for noise_path in sorted(noise_dir.glob("*.pt")):
            image_path = image_dir / noise_path.name
            if image_path.exists():
                samples.append({
                    "noise": str(noise_path),
                    "image": str(image_path),
                })

        return samples

    def __getitem__(self, idx: int) -> dict:
        sample_info = self.samples[idx]

        noise = torch.load(sample_info["noise"], map_location="cpu", weights_only=True)
        image = torch.load(sample_info["image"], map_location="cpu", weights_only=True)

        return {
            "latent": image,       # Target (data end)
            "noise_pair": noise,   # Paired noise (noise end)
        }
